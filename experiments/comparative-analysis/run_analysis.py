import argparse
import json
import re
from collections import Counter

# Define a set of spatial keywords to track, relevant to VQASynth's goals
SPATIAL_KEYWORDS = [
    "left",
    "right",
    "behind",
    "front",
    "top",
    "bottom",
    "above",
    "below",
    "near",
    "far",
    "close",
    "between",
    "inside",
    "outside",
    "on",
    "beside",
]


def load_dataset(file_path):
    """Loads a JSONL dataset."""
    data = []
    try:
        with open(file_path, "r") as f:
            for line in f:
                data.append(json.loads(line))
    except FileNotFoundError:
        print(f"Error: File not found at {file_path}")
        exit(1)
    except json.JSONDecodeError as e:
        print(f"Error decoding JSON in {file_path}: {e}")
        exit(1)
    return data


def analyze_conversations(conversations):
    """Analyzes a list of conversations for a single data sample."""
    question_len = 0
    answer_len = 0
    keywords = Counter()

    # Assuming alternating human/gpt turns
    for turn in conversations:
        text = turn.get("value", "").lower()
        if turn.get("from") == "human":
            question_len += len(text.split())
        elif turn.get("from") == "gpt":
            answer_len += len(text.split())

        # Find all occurrences of keywords in this turn's text
        found_keywords = re.findall(r"\b(" + "|".join(SPATIAL_KEYWORDS) + r")\b", text)
        keywords.update(found_keywords)

    return question_len, answer_len, keywords


def calculate_stats(dataset):
    """Calculates aggregate statistics for an entire dataset."""
    total_samples = len(dataset)
    if total_samples == 0:
        return {
            "sample_count": 0,
            "avg_question_length_words": 0,
            "avg_answer_length_words": 0,
            "keyword_frequency": {},
        }

    total_question_len = 0
    total_answer_len = 0
    total_keywords = Counter()

    for sample in dataset:
        conversations = sample.get("conversations", [])
        q_len, a_len, keywords = analyze_conversations(conversations)
        total_question_len += q_len
        total_answer_len += a_len
        total_keywords.update(keywords)

    stats = {
        "sample_count": total_samples,
        "avg_question_length_words": round(total_question_len / total_samples, 2),
        "avg_answer_length_words": round(total_answer_len / total_samples, 2),
        "keyword_frequency": dict(sorted(total_keywords.items())),
    }
    return stats


def main():
    parser = argparse.ArgumentParser(
        description="Compare two VQA datasets generated by the VQASynth pipeline.",
        formatter_class=argparse.RawTextHelpFormatter,
    )
    parser.add_argument(
        "--dataset1",
        required=True,
        help="Path to the first JSONL dataset (e.g., baseline).",
    )
    parser.add_argument(
        "--dataset2",
        required=True,
        help="Path to the second JSONL dataset (e.g., experimental).",
    )
    parser.add_argument(
        "--output",
        required=True,
        help="Path to save the comparison report as a JSON file.",
    )
    args = parser.parse_args()

    print(f"Loading dataset 1 from {args.dataset1}...")
    dataset1 = load_dataset(args.dataset1)

    print(f"Loading dataset 2 from {args.dataset2}...")
    dataset2 = load_dataset(args.dataset2)

    print("Analyzing dataset 1...")
    stats1 = calculate_stats(dataset1)

    print("Analyzing dataset 2...")
    stats2 = calculate_stats(dataset2)

    report = {
        "comparison_summary": {
            "dataset_A": {"path": args.dataset1, "stats": stats1},
            "dataset_B": {"path": args.dataset2, "stats": stats2},
        }
    }

    print(f"Saving report to {args.output}...")
    with open(args.output, "w") as f:
        json.dump(report, f, indent=2)

    print("\nAnalysis complete. Report saved.")


if __name__ == "__main__":
    main()
