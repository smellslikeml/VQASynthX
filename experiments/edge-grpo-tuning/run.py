import torch
from datasets import load_dataset
from transformers import AutoTokenizer
from trl import PPOConfig, PPOTrainer, AutoModelForCausalLMWithValueHead

def main():
    """
    Main function to run the EDGE-GRPO inspired RL experiment.
    """
    # --- 1. Configuration ---
    # PPO configuration with a small model for testability
    config = PPOConfig(
        model_name="gpt2",
        learning_rate=1.4e-5,
        batch_size=16,
        mini_batch_size=4,
        log_with="tensorboard",
        project_kwargs={"logging_dir": "./logs/edge-grpo-tuning"},
        ppo_epochs=4,
        kl_penalty="kl",
        target_kl=0.1,
        init_kl_coef=0.2,
    )

    # EDGE-GRPO inspired hyperparameter for entropy bonus
    # This weight controls the influence of response diversity on the reward signal.
    entropy_beta = 0.1

    # --- 2. Load Model and Tokenizer ---
    # In a full VLM application, this would be a model like LLaVA or Florence-2.
    # We use a standard CausalLM with a ValueHead for PPO training.
    model = AutoModelForCausalLMWithValueHead.from_pretrained(config.model_name)
    tokenizer = AutoTokenizer.from_pretrained(config.model_name)
    tokenizer.pad_token = tokenizer.eos_token
    model.config.pad_token_id = tokenizer.eos_token_id

    # --- 3. Load and Prepare Dataset ---
    # Using a dataset generated by VQASynth to test spatial reasoning.
    dataset = load_dataset("remyxai/OpenSpaces_MC_R1", split="train")

    # For a quick and minimal test, we select a small subset.
    dataset = dataset.shuffle(seed=42).select(range(200))

    def tokenize_sample(sample):
        """Format the prompt for the model."""
        # This would be more complex for a VLM, involving image tokens.
        prompt = f"Question: {sample['query']}\nAnswer:"
        sample["input_ids"] = tokenizer.encode(prompt)
        # The 'query' field is kept for logging and reward calculation.
        sample["query_text"] = prompt
        return sample

    dataset = dataset.map(tokenize_sample, batched=False)
    dataset.set_format(type="torch")

    # --- 4. Initialize PPO Trainer ---
    ppo_trainer = PPOTrainer(
        config=config,
        model=model,
        ref_model=None,
        tokenizer=tokenizer,
        dataset=dataset,
    )

    # --- 5. Training Loop ---
    generation_kwargs = {
        "min_length": -1,
        "top_k": 0.0,
        "top_p": 1.0,
        "do_sample": True,
        "pad_token_id": tokenizer.eos_token_id,
        "max_new_tokens": 32,
    }

    print("Starting RL training with Entropy-Driven Rewards...")
    for i, batch in enumerate(ppo_trainer.dataloader):
        query_tensors = batch["input_ids"]

        # Generate model responses
        response_tensors, logprobs = ppo_trainer.generate(
            query_tensors, return_prompt=False, log_probs=True, **generation_kwargs
        )
        batch["response"] = tokenizer.batch_decode(response_tensors, skip_special_tokens=True)

        # --- 6. Compute Reward (with Entropy Bonus) ---
        # Base reward: +1.0 if the model's response contains the correct answer.
        base_rewards = []
        for response_text, ground_truth in zip(batch["response"], batch["multiple_choice_answer"]):
            reward = 1.0 if ground_truth.lower() in response_text.lower() else 0.0
            base_rewards.append(torch.tensor(reward, device=model.device))

        # CORE OF THE EXPERIMENT: Add Entropy-Driven Reward inspired by EDGE-GRPO
        # This encourages policy exploration and diversity, mitigating advantage collapse.
        # We use the negative log-probability of the generated sequence as a proxy for entropy.
        # Higher entropy (more surprising sequences) results in a larger reward bonus.
        entropy_bonus = -logprobs.mean(dim=1)
        
        # Normalize the entropy bonus to have zero mean and unit variance
        # This prevents the bonus from overpowering the primary task reward.
        normalized_entropy_bonus = (entropy_bonus - entropy_bonus.mean()) / (entropy_bonus.std() + 1e-8)
        
        # Combine rewards
        rewards = [
            r + entropy_beta * e for r, e in zip(base_rewards, normalized_entropy_bonus)
        ]

        # --- 7. PPO Optimization Step ---
        stats = ppo_trainer.step(query_tensors, response_tensors, rewards)
        
        # Log statistics for analysis
        log_stats = {'env/reward_mean': torch.mean(torch.stack(rewards)).item(), 'env/base_reward_mean': torch.mean(torch.stack(base_rewards)).item()}
        stats.update(log_stats)
        ppo_trainer.log_stats(stats, batch, torch.stack(rewards).cpu().numpy().tolist())
        print(f"Step {i+1}: Mean Reward: {stats['env/reward_mean']:.3f}, Mean Base Reward: {stats['env/base_reward_mean']:.3f}")

    print("Training finished.")

if __name__ == "__main__":
    main()
