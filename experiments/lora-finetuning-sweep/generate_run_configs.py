#!/usr/bin/env python3
"""
Generate a shell script for a hyperparameter sweep of the LoRA fine-tuning stage,
_inspired by `generate_configs.py` from the MedARC-AI/algonauts2025 project.
"""

import os
import itertools
from pathlib import Path

# Define the hyperparameter grid for LoRA fine-tuning
HPARAMS_GRID = {
    "lora_r": [8, 16, 32],
    "lora_alpha": [16, 32, 64],
    "learning_rate": ["1e-5", "2e-5", "5e-5"],
    "num_train_epochs": [1, 2],
}

# Template for the run script
# Assumes the llava_train/entrypoint.sh can accept these as environment variables
# and that a dataset is mounted to /app/data and outputs are written to /app/output
RUN_COMMAND_TEMPLATE = """
echo "--- Starting run with LORA_R={lora_r} LORA_ALPHA={lora_alpha} LR={learning_rate} EPOCHS={num_train_epochs} ---"
docker run --rm --gpus all \\
  -v ./data:/app/data \\
  -v ./output/{run_name}:/app/output \\
  -e LORA_R={lora_r} \\
  -e LORA_ALPHA={lora_alpha} \\
  -e LEARNING_RATE={learning_rate} \\
  -e NUM_TRAIN_EPOCHS={num_train_epochs} \\
  vqasynth-llava-train:latest
"""


def generate_run_script():
    """Generates a shell script to run all hyperparameter combinations."""
    output_dir = Path(__file__).parent
    script_path = output_dir / "run_sweep.sh"

    # Get all combinations of hyperparameters
    keys, values = zip(*HPARAMS_GRID.items())
    param_combinations = [dict(zip(keys, v)) for v in itertools.product(*values)]

    print(f"Generating {len(param_combinations)} run configurations...")

    with open(script_path, "w") as f:
        f.write("#!/bin/bash\n")
        f.write("# This script was auto-generated by generate_run_configs.py\n")
        f.write("# It runs a hyperparameter sweep for LoRA fine-tuning.\n\n")
        f.write("set -e\n\n")  # Exit on error

        for params in param_combinations:
            run_name = f"run_r{params['lora_r']}_alpha{params['lora_alpha']}_lr{params['learning_rate']}_e{params['num_train_epochs']}"

            # Create the output directory for this run
            f.write(f"mkdir -p ./output/{run_name}\n")

            command = RUN_COMMAND_TEMPLATE.format(run_name=run_name, **params)
            f.write(command)
            f.write("\n")

    os.chmod(script_path, 0o755)  # Make the script executable
    print(f"✅ Successfully generated run script at: {script_path}")


if __name__ == "__main__":
    generate_run_script()
