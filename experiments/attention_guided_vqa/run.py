import torch
import torch.nn as nn
import torch.optim as optim
from PIL import Image
import requests
from transformers import AutoProcessor, LlavaForConditionalGeneration

# 1. SETUP: Model and Processor
# Using a real LLaVA model makes the experiment more authentic.
# We assume a machine capable of running it, as implied by the Docker-based workflow.
print("Loading LLaVA model and processor...")
model_id = "llava-hf/llava-1.5-7b-hf"

model = LlavaForConditionalGeneration.from_pretrained(
    model_id,
    torch_dtype=torch.float16,
    low_cpu_mem_usage=True,
    device_map="auto",  # Use GPU if available
)
processor = AutoProcessor.from_pretrained(model_id)
print("Model and processor loaded.")

# 2. DATA SIMULATION: Image, Prompt, and Target Attention Map
# In the full VQASynth pipeline, this data would be generated by previous stages.
# Here, we simulate it for a self-contained demonstration.
print("Simulating VQA data with target attention map...")
url = "http://images.cocodataset.org/val2017/000000039769.jpg"  # An image with two cats
image = Image.open(requests.get(url, stream=True).raw).convert("RGB")
prompt = (
    "<image>\nUSER: What is the spatial relationship between the two cats?\nASSISTANT:"
)
answer_text = (
    "The cat on the left is sitting near the cat on the right on the same couch."
)

# Simulate a target attention map.
# In a real scenario, this would be generated from segmentation masks of the objects in the prompt.
# LLaVA's vision tower processes images into a grid of patches (e.g., 24x24 for standard sizes).
# We create a target map at this patch resolution.
attention_patch_size = (24, 24)
target_attention_map = torch.zeros(
    attention_patch_size, device=model.device, dtype=torch.float16
)
# Highlight approximate regions for the two cats (top-left and center-right based on the image)
target_attention_map[4:12, 2:10] = 1.0  # Approximate area for left cat
target_attention_map[10:18, 12:22] = 1.0  # Approximate area for right cat

# Normalize to form a probability distribution, which is good practice for attention comparison.
if target_attention_map.sum() > 0:
    target_attention_map /= target_attention_map.sum()

# 3. ATTENTION GUIDANCE SETUP
# Define the loss functions and hyperparameters for the AGT approach.
vqa_loss_fn = nn.CrossEntropyLoss()
attention_loss_fn = (
    nn.MSELoss()
)  # Mean Squared Error is a simple choice for comparing attention maps.
attention_guidance_weight = 0.5  # Lambda hyperparameter to balance the two losses.

# Optimizer for fine-tuning
optimizer = optim.Adam(model.parameters(), lr=1e-6)

# 4. AGT TRAINING LOOP (Minimal Demo)
print("\nStarting single Attention-Guided Training step...")

# Pre-process inputs for the model
inputs = processor(text=prompt, images=image, return_tensors="pt").to(
    model.device, torch.float16
)
labels = processor.tokenizer(answer_text, return_tensors="pt").input_ids.to(
    model.device
)

# --- Training Step ---
optimizer.zero_grad()

# Forward pass: we must enable `output_attentions` to get intermediate vision representations.
outputs = model(**inputs, labels=labels, output_attentions=True)

# --- Loss Calculation ---

# a) VQA Loss (Standard Language Modeling Loss provided by the model)
vqa_loss = outputs.loss

# b) Attention Loss (The core of the AGT experiment)
# Extract attention weights from the vision tower's last layer.
# Shape is typically (batch_size, num_heads, seq_len, seq_len).
# seq_len is num_patches + 1 (for the [CLS] token).
vision_attentions = outputs.vision_model_outputs.attentions[-1]

# We average attention across all heads, focusing on the [CLS] token's attention to image patches.
# This indicates the model's overall visual focus.
# Indexing: [:, :, 0, 1:] -> all batches, all heads, [CLS] token's row, all patch columns.
model_attention_raw = vision_attentions[:, :, 0, 1:].mean(dim=1)

# Reshape from a flat sequence of patches to the 2D grid.
model_attention = model_attention_raw.view(1, *attention_patch_size).squeeze(0)

# Normalize the model's attention map to compare with the target.
if model_attention.sum() > 0:
    model_attention = model_attention / model_attention.sum()

attention_loss = attention_loss_fn(model_attention, target_attention_map)

# c) Combine losses
total_loss = vqa_loss + attention_guidance_weight * attention_loss

print(f"  -> VQA Loss: {vqa_loss.item():.4f}")
print(f"  -> Attention Loss: {attention_loss.item():.4f}")
print(f"  -> Total Combined Loss: {total_loss.item():.4f}")

# Backward pass and optimization step
total_loss.backward()
optimizer.step()

print("\nSingle training step completed successfully.")
print(
    "This demonstrates the calculation of a combined loss for VQA and Attention Guidance."
)
print("In a full run, this process would be repeated over an entire dataset.")
