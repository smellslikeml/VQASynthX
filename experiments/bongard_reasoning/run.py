import json
from jinja2 import Environment

# This prompt template is adapted from the Bongard-RWR+ repository.
# It uses a few-shot approach to guide a language model in solving a
# Bongard problem based on textual descriptions of the images.
# Source: https://github.com/pavonism/bongard-rwr-plus/blob/main/resources/prompts/d1s/v2/d1s-predict-1.jinja
PROMPT_TEMPLATE = """Use the descriptions below to solve the provided Bongard Problem. Respond in json using the following format.
EXAMPLE START
REQUEST
{
    "left_descriptions": [
        "A wired telephone with a coiled cord resting on a wooden table",
        "Power lines stretching across a clear blue sky with birds perched on them",
        "A clothes drying rack with colorful clothes hanging under the sunlight",
        "A swing hanging from a tree branch in a park with a child playing nearby",
        "A needle with thread passing through fabric, placed on a sewing table",
        "A pair of shoes with neatly tied laces placed on a tiled floor"
    ],
    "right_descriptions": [
        "A smartphone with a cracked screen lying on a desk with visible damage",
        "A bird flying freely in the sky with its wings spread wide",
        "A pile of folded clothes neatly stacked on a shelf in a wardrobe",
        "A park bench surrounded by fallen leaves in an autumn setting",
        "A piece of fabric with intricate embroidery placed on a sewing table",
        "A pair of slip-on sandals placed on a carpeted floor near a doorway"
    ],
    "test_description": "A sailboat with its sails fully open gliding across a calm lake under a clear sky"
}
RESPONSE
{
    "concept": "line is present vs no line is present",
    "explanation": "The first test image presents a sailboat with ropes hanging the sails. The ropes are lines, the same as items on the LEFT, like the telephone cord, power lines, and shoelaces.",
    "answer": "LEFT"

}
EXAMPLE END
EXAMPLE START
REQUEST
{{ request_json }}
RESPONSE
"""


def create_bongard_prompt(left_descriptions, right_descriptions, test_description):
    """
    Renders the Jinja2 template with the provided Bongard problem data.

    Args:
        left_descriptions (list[str]): Descriptions of images on the left side.
        right_descriptions (list[str]): Descriptions of images on the right side.
        test_description (str): Description of the test image.

    Returns:
        str: The fully rendered prompt ready to be sent to an LLM.
    """
    request_data = {
        "left_descriptions": left_descriptions,
        "right_descriptions": right_descriptions,
        "test_description": test_description,
    }

    # Use Jinja2 to render the template
    env = Environment()
    template = env.from_string(PROMPT_TEMPLATE)

    # We use json.dumps with indentation to format the JSON string nicely within the prompt
    rendered_prompt = template.render(request_json=json.dumps(request_data, indent=4))

    return rendered_prompt


def main():
    """
    Main function to demonstrate the creation of a Bongard problem prompt.
    This sets up a minimal, testable experiment based on the Bongard-RWR+ paper,
    which can be used to benchmark the abstract reasoning capabilities of models
    within the VQASynth framework.
    """
    print("--- Generating Bongard Problem Prompt for Abstract Reasoning ---")

    # Sample data for a new Bongard problem.
    # This data represents the "d1s" (description-to-side) task from the source repo.
    # In a real scenario, these descriptions would be generated by a VLM from the
    # Bongard-RWR+ image dataset.
    left_descriptions = [
        "A close-up shot of a white daisy with a yellow center against a blurred green background.",
        "A red rose with dewdrops on its petals, pictured in a garden setting.",
        "A sunflower facing the sun, with its bright yellow petals and dark center clearly visible.",
        "A vibrant purple iris flower in full bloom.",
        "A delicate pink cherry blossom branch against a clear blue sky.",
        "A bunch of lavender flowers tied with a ribbon.",
    ]
    right_descriptions = [
        "A green fern leaf with intricate patterns, seen in a forest.",
        "A close-up of a succulent plant with thick, fleshy leaves.",
        "A large, waxy leaf from a monstera plant, showing its characteristic holes.",
        "A view of a pine tree with its needles and cones.",
        "A blade of grass with a single drop of morning dew.",
        "A collection of fallen autumn leaves in shades of red, orange, and yellow.",
    ]
    test_description = "A tulip with red and yellow petals standing tall in a field."

    # Generate the prompt
    prompt = create_bongard_prompt(
        left_descriptions, right_descriptions, test_description
    )

    # Print the prompt to demonstrate the output.
    # This output would be fed into a large language model.
    print("\n--- Rendered LLM Prompt ---")
    print(prompt)
    print("---------------------------\n")
    print(
        "Experiment setup is complete. To test a model, send the above prompt to an LLM API and parse the JSON response."
    )


if __name__ == "__main__":
    main()
