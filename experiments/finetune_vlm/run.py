import subprocess
import shlex


def run_finetuning():
    """
    This script launches a VLM fine-tuning job using LLaMA-Factory.

    It is configured to perform LoRA-based supervised fine-tuning (SFT) on a
    VLM, using a dataset generated by the VQASynth pipeline. This provides an
    end-to-end workflow for enhancing a model's spatial reasoning capabilities
    by integrating LLaMA-Factory's training engine.
    """
    # --- Configuration ---
    # This configuration uses LoRA to efficiently fine-tune the LLaVA 1.5 7B model
    # on the `vqasynth_spacellava` dataset, which contains spatial reasoning examples.
    config = {
        "do_train": True,
        "model_name_or_path": "llava-hf/llava-1.5-7b-hf",
        "dataset": "remyxai/vqasynth_spacellava",
        "template": "llava",
        "finetuning_type": "lora",
        "lora_target": "all",
        "output_dir": "experiments/finetune_vlm/output",
        "overwrite_output_dir": True,
        "per_device_train_batch_size": 2,
        "gradient_accumulation_steps": 8,
        "learning_rate": 1e-4,
        "num_train_epochs": 1.0,
        "lr_scheduler_type": "cosine",
        "logging_steps": 10,
        "save_steps": 500,
        "fp16": True,
        "plot_loss": True,
        "report_to": "none",
    }

    # --- Build Command ---
    command = ["llamafactory-cli", "train"]

    for key, value in config.items():
        if isinstance(value, bool):
            if value:
                command.append(f"--{key}")
        else:
            command.append(f"--{key}")
            command.append(str(value))

    # --- Run Command ---
    print("--- LLaMA-Factory Command ---")
    # Use shlex.join for robust command line string construction
    print(shlex.join(command))
    print("-----------------------------")

    # Execute the command
    try:
        subprocess.run(command, check=True)
        print("\n--- Fine-tuning complete ---")
        print(f"LoRA adapter saved to: {config['output_dir']}")
    except subprocess.CalledProcessError as e:
        print(f"\n--- An error occurred during fine-tuning ---")
        print(f"Return code: {e.returncode}")
        exit(1)  # Exit with an error code
    except FileNotFoundError:
        print("\n--- Error: `llamafactory-cli` not found ---")
        print("Please ensure LLaMA-Factory is installed correctly in your environment.")
        exit(1)  # Exit with an error code


if __name__ == "__main__":
    run_finetuning()
