import logging
import os
from dataclasses import dataclass, field
from typing import Optional

import torch
from datasets import load_dataset
from transformers import (
    AutoModelForCausalLM,
    AutoTokenizer,
    HfArgumentParser,
    TrainingArguments,
    Trainer,
    BitsAndBytesConfig,
)
from peft import LoraConfig, get_peft_model

# This script is adapted from the SFT implementation in huggingface/open-r1.
# It provides a structured way to fine-tune a model on the datasets generated
# by the VQASynth pipeline, completing the loop from data synthesis to training.
# See: https://github.com/huggingface/open-r1/blob/main/src/open_r1/sft.py

logger = logging.getLogger(__name__)


@dataclass
class ModelArguments:
    """
    Arguments pertaining to which model/config/tokenizer we are going to fine-tune from.
    """

    model_name_or_path: str = field(
        metadata={
            "help": "Path to pretrained model or model identifier from huggingface.co/models"
        }
    )
    use_lora: bool = field(
        default=False,
        metadata={"help": "Whether to use LoRA for parameter-efficient training."},
    )
    lora_r: int = field(default=8, metadata={"help": "LoRA attention dimension."})
    lora_alpha: int = field(default=16, metadata={"help": "LoRA alpha parameter."})
    lora_dropout: float = field(
        default=0.05, metadata={"help": "LoRA dropout probability."}
    )


@dataclass
class DataArguments:
    """
    Arguments pertaining to what data we are going to input our model for training.
    """

    dataset_path: str = field(
        metadata={
            "help": "Path to the training data file (e.g., a .jsonl file generated by VQASynth)."
        }
    )
    max_seq_length: int = field(
        default=1024,
        metadata={
            "help": "The maximum total input sequence length after tokenization."
        },
    )


def main():
    parser = HfArgumentParser((ModelArguments, DataArguments, TrainingArguments))
    model_args, data_args, training_args = parser.parse_args_into_dataclasses()

    # --- 1. Load Dataset ---
    # VQASynth produces conversational data. We expect a JSONL file with 'conversations' field.
    # Example format: {"conversations": [{"from": "human", "value": "..."}, {"from": "gpt", "value": "..."}]}
    try:
        dataset = load_dataset("json", data_files=data_args.dataset_path, split="train")
        logger.info(
            f"Loaded dataset from {data_args.dataset_path} with {len(dataset)} examples."
        )
    except Exception as e:
        logger.error(f"Failed to load dataset: {e}")
        return

    # --- 2. Load Tokenizer and Model ---
    tokenizer = AutoTokenizer.from_pretrained(
        model_args.model_name_or_path, trust_remote_code=True
    )
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    model = AutoModelForCausalLM.from_pretrained(
        model_args.model_name_or_path,
        torch_dtype=torch.bfloat16,  # Inspired by open-r1 defaults for modern GPUs
        trust_remote_code=True,
        # For a full VLM, model loading would be different (e.g. LlavaForConditionalGeneration)
    )

    if model_args.use_lora:
        lora_config = LoraConfig(
            r=model_args.lora_r,
            lora_alpha=model_args.lora_alpha,
            lora_dropout=model_args.lora_dropout,
            bias="none",
            task_type="CAUSAL_LM",
            # For a VLM, target_modules would need to include vision tower parameters.
            target_modules=["q_proj", "v_proj"],
        )
        model = get_peft_model(model, lora_config)
        model.print_trainable_parameters()

    # --- 3. Preprocess and Tokenize Data ---
    def preprocess_function(examples):
        # Simple processing for conversational format. A more robust implementation
        # would use a chat template from the tokenizer.
        # This assumes a text-only representation for now. VLM training would require
        # handling image tokens like '<image>'.
        formatted_texts = []
        for conv in examples["conversations"]:
            text = ""
            for turn in conv:
                role = "User" if turn["from"] == "human" else "Assistant"
                text += f"{role}: {turn['value']}\n"
            formatted_texts.append(text)

        return tokenizer(
            formatted_texts,
            truncation=True,
            max_length=data_args.max_seq_length,
            padding="max_length",
        )

    with training_args.main_process_first(desc="dataset map pre-processing"):
        tokenized_dataset = dataset.map(
            preprocess_function,
            batched=True,
            remove_columns=dataset.column_names,
        )

    # --- 4. Initialize Trainer ---
    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=tokenized_dataset,
        tokenizer=tokenizer,
    )

    # --- 5. Train ---
    if training_args.do_train:
        logger.info("*** Starting Training ***")
        train_result = trainer.train()
        trainer.save_model()  # Saves the tokenizer too
        trainer.log_metrics("train", train_result.metrics)
        trainer.save_metrics("train", train_result.metrics)
        trainer.save_state()

    logger.info("*** Training Complete ***")


if __name__ == "__main__":
    main()
