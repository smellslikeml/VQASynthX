import torch
from PIL import Image
from transformers import AutoProcessor, LlavaForConditionalGeneration
import requests
import os

# This script proposes a new benchmark for temporal reasoning in VLMs,
# inspired by the LTLZinc framework (https://github.com/continual-nesy/LTLZinc).
# LTLZinc generates sequences of images governed by rules expressed in
# Linear Temporal Logic (LTL).
#
# EXPERIMENT CONCEPT:
# We will test a VLM's ability to understand a simple temporal rule.
# We'll use a sequence of images that follows the LTL rule: "G (cat -> X dog)"
# which translates to "Globally, if the current image is a cat, the next image is a dog."
#
# This establishes a new evaluation dimension for VQASynth, moving beyond static
# spatial reasoning to dynamic temporal understanding.

def setup_model():
    """Load the VLM and processor."""
    model_id = "llava-hf/llava-1.5-7b-hf"
    model = LlavaForConditionalGeneration.from_pretrained(
        model_id,
        torch_dtype=torch.float16,
        low_cpu_mem_usage=True,
    ).to("cuda")
    processor = AutoProcessor.from_pretrained(model_id)
    return model, processor

def prepare_data():
    """
    Prepare a sample image sequence following the rule "cat is always followed by dog".
    In a full implementation, this data would be generated by LTLZinc.
    """
    # URLs for sample images
    image_urls = {
        "cat": "http://images.cocodataset.org/val2017/000000039769.jpg",
        "dog": "http://images.cocodataset.org/val2017/000000032924.jpg",
    }

    # Create image directory if it doesn't exist
    os.makedirs("temp_images", exist_ok=True)
    
    # Download images
    for name, url in image_urls.items():
        path = f"temp_images/{name}.jpg"
        if not os.path.exists(path):
            try:
                response = requests.get(url, timeout=10)
                response.raise_for_status()
                with open(path, "wb") as f:
                    f.write(response.content)
            except requests.RequestException as e:
                print(f"Failed to download {name}.jpg: {e}")
                return None

    # Sequence: [cat, dog, cat, dog]
    sequence_paths = [
        "temp_images/cat.jpg",
        "temp_images/dog.jpg",
        "temp_images/cat.jpg",
        "temp_images/dog.jpg"
    ]
    images = [Image.open(path).convert("RGB") for path in sequence_paths]
    
    return images

def run_benchmark(model, processor, images):
    """
    Run the temporal reasoning benchmark.
    We show the model the sequence of images and ask it to identify the rule.
    """
    # The VLM is prompted with the sequence of images.
    # We use a special token <image> for each image in the sequence.
    image_tokens = " ".join(["<image>" for _ in images])
    
    prompt = (
        f"USER: {image_tokens}\n"
        "Here is a sequence of four images. Observe the pattern. "
        "Which of the following rules best describes the sequence?\n"
        "A) A cat is always followed by another cat.\n"
        "B) A cat is always followed by a dog.\n"
        "C) A dog is always followed by another dog.\n"
        "Respond with only the letter of the correct option. ASSISTANT:"
    )

    inputs = processor(text=prompt, images=images, return_tensors="pt").to("cuda", torch.float16)

    # Generate response
    output = model.generate(**inputs, max_new_tokens=20)
    response_text = processor.decode(output[0], skip_special_tokens=True)
    
    # Extract just the assistant's response
    final_answer = response_text.split("ASSISTANT:")[-1].strip()

    print(f"Model was shown a sequence of [cat, dog, cat, dog].")
    print(f"Question: Which rule describes the sequence?")
    print(f"  A) A cat is always followed by another cat.")
    print(f"  B) A cat is always followed by a dog.")
    print(f"  C) A dog is always followed by another dog.")
    print("-" * 20)
    print(f"Model's Answer: {final_answer}")
    
    return final_answer

def main():
    print("Setting up model...")
    model, processor = setup_model()
    
    print("\nPreparing image sequence data...")
    images = prepare_data()
    
    if images:
        print("\nRunning temporal reasoning benchmark...")
        run_benchmark(model, processor, images)
        print("\nBenchmark finished.")
    else:
        print("Could not run benchmark due to data preparation failure.")

if __name__ == "__main__":
    main()
