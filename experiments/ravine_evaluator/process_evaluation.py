import argparse
import json
import re
import numpy as np
from datasets import load_from_disk
from tqdm import tqdm

# Conversion factors to meters
CONVERSION_FACTORS = {
    "meters": 1.0,
    "meter": 1.0,
    "m": 1.0,
    "centimeters": 0.01,
    "centimeter": 0.01,
    "cm": 0.01,
    "feet": 0.3048,
    "foot": 0.3048,
    "ft": 0.3048,
    "inches": 0.0254,
    "inch": 0.0254,
}

def extract_distance_from_answer(answer_text):
    """
    Extracts a numerical distance and its unit from a VLM's answer string.
    Handles single numbers, ranges (e.g., "2-3 feet"), and common units.
    """
    # Look for patterns like "2.5 meters", "2-3 ft", "10 cm"
    # This regex captures a number (or range) and an optional unit
    pattern = r'(\d+\.?\d*(?:\s*-\s*\d+\.?\d*)?)\s*([a-zA-Z]+)'
    match = re.search(pattern, answer_text)

    if not match:
        return None, None

    value_str, unit_str = match.groups()
    unit = unit_str.lower().strip()

    # Handle numeric ranges by taking the average
    if '-' in value_str:
        low, high = map(float, value_str.split('-'))
        value = (low + high) / 2.0
    else:
        value = float(value_str)

    # Find the canonical unit
    for canonical_unit, factor in CONVERSION_FACTORS.items():
        if unit.startswith(canonical_unit.rstrip('s')): # handles pluralization
            return value, canonical_unit
    
    return None, None


def evaluate_consistency(dataset, tolerance=0.50):
    """
    Evaluates the geometric consistency of VQA samples.
    Compares the distance stated in the answer to the calculated Euclidean distance
    from 3D coordinates.
    """
    results = []
    consistent_count = 0
    inconsistent_count = 0
    unparsable_count = 0

    for i, example in enumerate(tqdm(dataset, desc="Evaluating Samples")):
        answer = example.get("answer")
        obj1_coords = example.get("obj1_3d_coords")
        obj2_coords = example.get("obj2_3d_coords")

        if not all([answer, obj1_coords, obj2_coords]):
            continue
        
        obj1_coords = np.array(obj1_coords)
        obj2_coords = np.array(obj2_coords)

        calculated_distance_m = np.linalg.norm(obj1_coords - obj2_coords)

        stated_value, stated_unit_key = extract_distance_from_answer(answer)
        
        if stated_value is None or stated_unit_key is None:
            unparsable_count += 1
            results.append({
                "id": i,
                "status": "unparsable_answer",
                "answer": answer,
                "calculated_distance_m": calculated_distance_m,
            })
            continue

        conversion_factor = CONVERSION_FACTORS.get(stated_unit_key, 1.0)
        stated_distance_m = stated_value * conversion_factor
        
        error = abs(stated_distance_m - calculated_distance_m)
        relative_error = error / max(calculated_distance_m, 1e-6)

        is_consistent = relative_error <= tolerance
        
        if is_consistent:
            status = "consistent"
            consistent_count += 1
        else:
            status = "inconsistent"
            inconsistent_count += 1

        results.append({
            "id": i,
            "status": status,
            "answer": answer,
            "stated_distance_m": stated_distance_m,
            "calculated_distance_m": calculated_distance_m,
            "relative_error": relative_error,
        })
    
    summary = {
        "total_samples_evaluated": consistent_count + inconsistent_count,
        "consistent": consistent_count,
        "inconsistent": inconsistent_count,
        "unparsable_answer": unparsable_count,
        "consistency_rate": consistent_count / max(1, consistent_count + inconsistent_count),
        "tolerance": tolerance
    }
    
    return summary, results


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Validate geometric consistency of VQASynth datasets.")
    parser.add_argument("--dataset_path", type=str, required=True, help="Path to the Hugging Face dataset directory generated by VQASynth.")
    parser.add_argument("--output_file", type=str, default="evaluation_results.json", help="Path to save the detailed evaluation results.")
    parser.add_argument("--tolerance", type=float, default=0.5, help="Relative error tolerance for consistency check (e.g., 0.5 for 50%).")
    
    args = parser.parse_args()

    print(f"Loading dataset from {args.dataset_path}...")
    try:
        generated_dataset = load_from_disk(args.dataset_path)
    except Exception as e:
        print(f"Failed to load dataset. Ensure it's a valid dataset saved with `save_to_disk`. Error: {e}")
        exit(1)

    required_columns = ["answer", "obj1_3d_coords", "obj2_3d_coords"]
    if not all(col in generated_dataset.column_names for col in required_columns):
        print(f"Error: Dataset must contain the following columns: {required_columns}")
        exit(1)

    print("Starting evaluation...")
    summary, detailed_results = evaluate_consistency(generated_dataset, tolerance=args.tolerance)

    print("\n--- Evaluation Summary ---")
    print(json.dumps(summary, indent=2))

    with open(args.output_file, 'w') as f:
        json.dump({
            "summary": summary,
            "details": detailed_results
        }, f, indent=2)

    print(f"\nDetailed results saved to {args.output_file}")
